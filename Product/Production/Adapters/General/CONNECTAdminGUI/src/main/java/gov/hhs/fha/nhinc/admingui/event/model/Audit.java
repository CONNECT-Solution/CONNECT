package gov.hhs.fha.nhinc.admingui.event.model;

/**
 *
 * @author achidamb
 */
public class Audit {

    private long id;
    private String eventType;
    private String eventTimestamp;
    private String userId;
    private String messageId;
    private String remoteHcid;
    private String message;
    private String relatesTo;
    private String direction;
    private String eventId;

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getMessageId() {
        return messageId;
    }

    public void setMessageId(String messageId) {
        this.messageId = messageId;
    }

    public String getRemoteHcid() {
        return remoteHcid;
    }

    public void setRemoteHcid(String remoteHcid) {
        this.remoteHcid = remoteHcid;
    }

    public String getEventType() {
        return eventType;
    }

    public void setEventType(String eventType) {
        this.eventType = eventType;
    }

    /**
     * @return the eventTimestamp
     */
    public String getEventTimestamp() {
        return eventTimestamp;
    }

    /**
     * @param eventTimestamp the eventTimestamp to set
     */
    public void setEventTimestamp(String eventTimestamp) {
        this.eventTimestamp = eventTimestamp;
    }

    /**
     * @return the message
     */
    public String getMessage() {
        return message;
    }

    /**
     * @param message the message to set
     */
    public void setMessage(String message) {
        this.message = message;
    }

    /**
     * @return the relatesTo
     */
    public String getRelatesTo() {
        return relatesTo;
    }

    /**
     * @param relatesTo the relatesTo to set
     */
    public void setRelatesTo(String relatesTo) {
        this.relatesTo = relatesTo;
    }

    /**
     * @return the direction
     */
    public String getDirection() {
        return direction;
    }

    /**
     * @param direction the direction to set
     */
    public void setDirection(String direction) {
        this.direction = direction;
    }

    /**
     * @return the eventId
     */
    public String getEventId() {
        return eventId;
    }

    /**
     * @param eventId the eventId to set
     */
    public void setEventId(String eventId) {
        this.eventId = eventId;
    }

    /**
     * @return the id
     */
    public long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(long id) {
        this.id = id;
    }
}
